fn advice_label(a: Advice) -> &'static str {
    match a {
        Advice::QuickScan => "Quick Scan",
        Advice::Update => "Update",
        Advice::Quarantine => "Review",
        Advice::Review => "Open Scan",
    }
}

fn sev_color(s: Severity) -> Color32 {
    match s {
        Severity::Low => DIM,
        Severity::Medium => AMBER,
        Severity::High => Color32::from_rgb(0xff, 0x6a, 0x3d),
        Severity::Critical => ACCENT,
    }
}

fn sev_tag(s: Severity) -> &'static str {
    match s {
        Severity::Low => "LOW ",
        Severity::Medium => "MED ",
        Severity::High => "HIGH",
        Severity::Critical => "CRIT",
    }
}

fn truncate(s: &str, max: usize) -> String {
    if s.chars().count() <= max {
        s.to_string()
    } else {
        let kept: String = s
            .chars()
            .rev()
            .take(max)
            .collect::<Vec<_>>()
            .into_iter()
            .rev()
            .collect();
        format!("…{kept}")
    }
}

fn now_unix() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0)
}

fn time_ago(then: u64) -> String {
    let secs = now_unix().saturating_sub(then);
    match secs {
        0..=59 => "just now".to_string(),
        60..=3599 => format!("{} min ago", secs / 60),
        3600..=86_399 => format!("{} h ago", secs / 3600),
        _ => format!("{} d ago", secs / 86_400),
    }
}
