// ---- palette -------------------------------------------------------------
const BG: Color32 = Color32::from_rgb(0x12, 0x16, 0x1d);
const PANEL: Color32 = Color32::from_rgb(0x18, 0x1d, 0x26);
const CARD: Color32 = Color32::from_rgb(0x1f, 0x26, 0x31);
const TEXT: Color32 = Color32::from_rgb(0xe6, 0xe9, 0xef);
const DIM: Color32 = Color32::from_rgb(0x93, 0x9f, 0xb0);
const ACCENT: Color32 = Color32::from_rgb(0xe1, 0x1d, 0x2a); // Talos red
const GREEN: Color32 = Color32::from_rgb(0x2b, 0xd6, 0x6a);
const AMBER: Color32 = Color32::from_rgb(0xff, 0xb0, 0x20);

fn main() -> eframe::Result<()> {
    let options = eframe::NativeOptions {
        viewport: egui::ViewportBuilder::default()
            .with_inner_size([1040.0, 700.0])
            .with_min_inner_size([880.0, 600.0])
            .with_icon(std::sync::Arc::new(load_icon()))
            .with_title("Talos EPP — Endpoint Protection"),
        ..Default::default()
    };
    eframe::run_native(
        "Talos EPP",
        options,
        Box::new(|cc| {
            install_theme(&cc.egui_ctx);
            Ok(Box::new(TalosApp::new()))
        }),
    )
}

/// The app icon for the window/taskbar, as raw 64x64 RGBA generated by
/// `tools/make_icon.py` (the same artwork embedded into the .exe).
fn load_icon() -> egui::IconData {
    egui::IconData {
        rgba: include_bytes!("../assets/icon-64.rgba").to_vec(),
        width: 64,
        height: 64,
    }
}

fn install_theme(ctx: &egui::Context) {
    let mut v = egui::Visuals::dark();
    v.panel_fill = PANEL;
    v.window_fill = PANEL;
    v.extreme_bg_color = BG;
    v.override_text_color = Some(TEXT);
    v.selection.bg_fill = ACCENT.linear_multiply(0.55);
    v.widgets.hovered.bg_fill = CARD;
    v.widgets.inactive.bg_fill = CARD;
    v.widgets.active.bg_fill = ACCENT;
    ctx.set_visuals(v);

    let mut style = (*ctx.style()).clone();
    style.spacing.item_spacing = egui::vec2(10.0, 10.0);
    style.spacing.button_padding = egui::vec2(12.0, 8.0);
    ctx.set_style(style);
}

#[derive(Clone, Copy, PartialEq, Eq)]
enum View {
    Dashboard,
    Protection,
    Scan,
    Quarantine,
    Activity,
    Intel,
    Settings,
    About,
}

#[derive(Clone, Copy)]
struct Done {
    files: u64,
    malicious: u64,
    suspicious: u64,
    ms: u64,
    bytes: u64,
}

/// A one-click action the Security Advisor can recommend.
#[derive(Clone, Copy)]
enum Advice {
    QuickScan,
    Update,
    Quarantine,
    Review,
}

struct TalosApp {
    view: View,
    version: String,
    tenant: Option<String>,
    config: TalosConfig,
    hashes: usize,
    yara: usize,
    quarantined: usize,

    custom_path: String,
    new_exclusion: String,

    rx: Option<Receiver<ScanMsg>>,
    scanning: bool,
    scan_label: String,
    scanned: u64,
    current: String,
    threats: Vec<ScanReport>,
    last: Option<Done>,
    last_scan_unix: u64,
    status: String,
    sig_source: String,
    last_update_unix: u64,
    updating: bool,
    update_rx: Option<Receiver<scanner_core::UpdateReport>>,

    q_items: Vec<scanner_core::QuarantineEntry>,
    q_loaded: bool,

    activity: Vec<history::Event>,
    activity_loaded: bool,

    realtime: Option<engine_glue::RealtimeHandle>,
    realtime_paths: usize,
    realtime_hits: u64,

    intel_query: String,
    intel_source: String,
    intel_lines: Vec<String>,
    intel_busy: bool,
    intel_rx: Option<Receiver<Result<Vec<scanner_core::IntelReport>, String>>>,

    /// The Talos bronze-guardian emblem, lazily uploaded as a GPU texture.
    hero_tex: Option<egui::TextureHandle>,
}

impl TalosApp {
    fn new() -> Self {
        let (hashes, yara, quarantined) = engine_glue::inventory_counts();
        Self {
            view: View::Dashboard,
            version: env!("CARGO_PKG_VERSION").to_string(),
            tenant: std::env::var("TALOS_TENANT").ok().filter(|s| !s.is_empty()),
            config: TalosConfig::load(),
            hashes,
            yara,
            quarantined,
            custom_path: String::new(),
            new_exclusion: String::new(),
            rx: None,
            scanning: false,
            scan_label: String::new(),
            scanned: 0,
            current: String::new(),
            threats: Vec::new(),
            last: None,
            last_scan_unix: 0,
            status: "Ready.".to_string(),
            sig_source: engine_glue::signatures_source(),
            last_update_unix: 0,
            updating: false,
            update_rx: None,
            q_items: Vec::new(),
            q_loaded: false,
            activity: Vec::new(),
            activity_loaded: false,
            realtime: None,
            realtime_paths: 0,
            realtime_hits: 0,
            intel_query: String::new(),
            intel_source: String::new(),
            intel_lines: Vec::new(),
            intel_busy: false,
            intel_rx: None,
            hero_tex: None,
        }
    }

    fn start_realtime(&mut self) {
        if self.realtime.is_some() {
            return;
        }
        self.realtime_hits = 0;
        self.realtime = Some(engine_glue::start_realtime(engine_glue::quick_scan_paths()));
        self.status = "Real-time monitoring starting…".to_string();
    }

    fn stop_realtime(&mut self) {
        if let Some(h) = self.realtime.take() {
            h.stop();
        }
        self.realtime_paths = 0;
        self.status = "Real-time monitoring stopped.".to_string();
    }

    fn do_intel(&mut self) {
        let q = self.intel_query.trim().to_string();
        if q.is_empty() || self.intel_busy {
            return;
        }
        self.intel_busy = true;
        self.intel_lines.clear();
        self.intel_source.clear();
        self.intel_rx = Some(engine_glue::start_intel(q));
    }

    fn refresh_inventory(&mut self) {
        let (h, y, q) = engine_glue::inventory_counts();
        self.hashes = h;
        self.yara = y;
        self.quarantined = q;
    }

    fn reload_quarantine(&mut self) {
        self.q_items = scanner_core::Quarantine::open(engine_glue::quarantine_dir())
            .and_then(|q| q.list())
            .unwrap_or_default();
        self.q_loaded = true;
    }

    fn reload_activity(&mut self) {
        self.activity = history::recent(200);
        self.activity_loaded = true;
    }

    /// Fetch signature feeds on a background thread; applied in `poll`.
    fn do_update(&mut self) {
        if self.updating {
            return;
        }
        self.updating = true;
        self.status = "Updating signatures from feeds…".to_string();
        self.update_rx = Some(engine_glue::start_update());
    }

    fn start(&mut self, targets: Vec<PathBuf>, label: &str) {
        if self.scanning {
            return;
        }
        if targets.is_empty() {
            self.status = format!("No targets found for {label} scan.");
            return;
        }
        self.threats.clear();
        self.scanned = 0;
        self.current.clear();
        self.last = None;
        self.scan_label = label.to_string();
        self.scanning = true;
        self.status = format!("Running {label} scan…");
        self.rx = Some(engine_glue::start_scan(targets));
        self.view = View::Scan;
    }

    fn apply_advice(&mut self, a: Advice) {
        match a {
            Advice::QuickScan => self.start(engine_glue::quick_scan_paths(), "Quick"),
            Advice::Update => self.do_update(),
            Advice::Quarantine => self.view = View::Quarantine,
            Advice::Review => self.view = View::Scan,
        }
    }

    /// Contextual recommendations computed from real state (Bitdefender-style).
    fn recommendations(&self) -> Vec<(String, Option<Advice>)> {
        let mut v = Vec::new();
        let threats = self.last.map(|d| d.malicious + d.suspicious).unwrap_or(0);
        if threats > 0 {
            v.push((
                format!("{threats} detection(s) in your last scan — review and quarantine."),
                Some(Advice::Review),
            ));
        }
        if self.quarantined > 0 {
            v.push((
                format!("{} item(s) waiting in Quarantine.", self.quarantined),
                Some(Advice::Quarantine),
            ));
        }
        if self.last_scan_unix == 0 {
            v.push((
                "You haven't run a scan yet — start with a Quick Scan.".to_string(),
                Some(Advice::QuickScan),
            ));
        }
        if self.last_update_unix == 0 {
            v.push((
                "Update signatures to pull the latest threat intelligence.".to_string(),
                Some(Advice::Update),
            ));
        }
        if v.is_empty() {
            v.push((
                "You're all set — protection is active and definitions are loaded.".to_string(),
                None,
            ));
        }
        v
    }

    fn poll(&mut self, ctx: &egui::Context) {
        let mut finished = false;
        if let Some(rx) = &self.rx {
            loop {
                match rx.try_recv() {
                    Ok(ScanMsg::Progress { scanned, current }) => {
                        self.scanned = scanned;
                        self.current = current;
                    }
                    Ok(ScanMsg::Threat(r)) => {
                        self.scanned += 1;
                        if self.threats.len() < 5000 {
                            self.threats.push(*r);
                        }
                    }
                    Ok(ScanMsg::Done {
                        files,
                        malicious,
                        suspicious,
                        ms,
                        bytes,
                    }) => {
                        self.last = Some(Done {
                            files,
                            malicious,
                            suspicious,
                            ms,
                            bytes,
                        });
                        self.last_scan_unix = now_unix();
                        self.scanning = false;
                        self.status = format!(
                            "{} scan complete — {malicious} malicious, {suspicious} suspicious in {files} files",
                            self.scan_label
                        );
                        finished = true;
                    }
                    Ok(ScanMsg::Failed(e)) => {
                        self.scanning = false;
                        self.status = format!("Scan failed: {e}");
                        finished = true;
                    }
                    Err(TryRecvError::Empty) => break,
                    Err(TryRecvError::Disconnected) => {
                        self.scanning = false;
                        finished = true;
                        break;
                    }
                }
            }
        }
        if finished {
            self.rx = None;
            self.refresh_inventory();
            self.activity_loaded = false;
        }

        // Drain a background feed update, if running.
        if let Some(rx) = &self.update_rx {
            match rx.try_recv() {
                Ok(report) => {
                    self.updating = false;
                    self.update_rx = None;
                    self.refresh_inventory();
                    self.sig_source = engine_glue::signatures_source();
                    self.last_update_unix = now_unix();
                    self.activity_loaded = false;
                    let detail = report.messages.join(" · ");
                    self.status = format!(
                        "Update complete — {detail}  →  {} hash signatures, {} YARA files",
                        self.hashes, self.yara
                    );
                }
                Err(TryRecvError::Empty) => {}
                Err(TryRecvError::Disconnected) => {
                    self.updating = false;
                    self.update_rx = None;
                    self.status = "Update failed.".to_string();
                }
            }
        }

        // Drain real-time monitor events (collect first to avoid borrow issues).
        let mut rt_events = Vec::new();
        if let Some(handle) = &self.realtime {
            while let Ok(msg) = handle.rx.try_recv() {
                rt_events.push(msg);
            }
        }
        for msg in rt_events {
            match msg {
                engine_glue::RealtimeMsg::Started(n) => {
                    self.realtime_paths = n;
                    self.status = format!("Real-time monitoring active — watching {n} folder(s).");
                }
                engine_glue::RealtimeMsg::Detection(r) => {
                    self.realtime_hits += 1;
                    self.status = format!("Real-time: detection in {}", r.path);
                    if self.threats.len() < 5000 {
                        self.threats.push(*r);
                    }
                    self.activity_loaded = false;
                }
                engine_glue::RealtimeMsg::Error(e) => {
                    self.status = format!("Real-time error: {e}");
                    self.realtime = None;
                }
            }
        }

        // Drain a threat-intel lookup, if running.
        let intel_result = self.intel_rx.as_ref().and_then(|rx| rx.try_recv().ok());
        if let Some(result) = intel_result {
            self.intel_busy = false;
            self.intel_rx = None;
            match result {
                Ok(reports) => {
                    let known = reports.iter().filter(|r| r.found).count();
                    self.intel_source =
                        format!("{} provider(s) · {known} with a record", reports.len());
                    let mut lines = Vec::new();
                    for r in reports {
                        lines.push(format!(
                            "── {} · {} ──",
                            r.source,
                            if r.found { "KNOWN" } else { "no record" }
                        ));
                        lines.extend(r.lines);
                    }
                    self.intel_lines = lines;
                }
                Err(e) => {
                    self.intel_source = "Lookup failed".to_string();
                    self.intel_lines = vec![e];
                }
            }
        }

        if self.scanning || self.updating || self.realtime.is_some() || self.intel_busy {
            ctx.request_repaint();
        }
    }
}

impl eframe::App for TalosApp {
    fn update(&mut self, ctx: &egui::Context, _frame: &mut eframe::Frame) {
        self.poll(ctx);

        // Upload the bronze-guardian emblem to a texture once.
        if self.hero_tex.is_none() {
            let img = egui::ColorImage::from_rgba_unmultiplied(
                [256, 256],
                include_bytes!("../assets/talos_hero.rgba"),
            );
            self.hero_tex = Some(ctx.load_texture("talos_hero", img, egui::TextureOptions::LINEAR));
        }

        egui::SidePanel::left("nav")
            .exact_width(212.0)
            .resizable(false)
            .frame(egui::Frame::none().fill(BG).inner_margin(16.0))
            .show(ctx, |ui| self.sidebar(ui));

        egui::CentralPanel::default()
            .frame(egui::Frame::none().fill(PANEL).inner_margin(22.0))
            .show(ctx, |ui| {
                egui::ScrollArea::vertical()
                    .auto_shrink([false, false])
                    .show(ui, |ui| match self.view {
                        View::Dashboard => self.dashboard(ui),
                        View::Protection => self.protection(ui),
                        View::Scan => self.scan_view(ui),
                        View::Quarantine => self.quarantine_view(ui),
                        View::Activity => self.activity_view(ui),
                        View::Intel => self.intel_view(ui),
                        View::Settings => self.settings_view(ui),
                        View::About => self.about(ui),
                    });
            });
    }
}

